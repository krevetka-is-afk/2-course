.data
buffer_result_s: .space 40           # Буфер для результирующей строки

.text
.globl string_sum

string_sum:
    # s5 - адрес первой строки
    # s6 - адрес второй строки
    # Возвращает: s11 - адрес результирующей строки

    la t0, buffer_result_s          # Указываем на начало буфера для результата

    # Копируем первую строку
copy_first:
    lb t1, (s5)                     # Читаем байт из первой строки
    beqz t1, copy_second            # Если конец строки, переходим к копированию второй
    sb t1, (t0)                     # Записываем байт в результирующий буфер
    addi s5, s5, 1                  # Следующий символ первой строки
    addi t0, t0, 1                  # Следующий символ в буфере
    j copy_first                    # Повторяем

copy_second:
    lb t1, (s6)                     # Читаем байт из второй строки
    beqz t1, finish                 # Если конец строки, завершаем
    sb t1, (t0)                     # Записываем байт в результирующий буфер
    addi s6, s6, 1                  # Следующий символ второй строки
    addi t0, t0, 1                  # Следующий символ в буфере
    j copy_second                   # Повторяем

finish:
    sb zero, (t0)                   # Записываем конец строки
    la s11, buffer_result_s         # Возвращаем адрес результирующей строки
    ret                             # Возврат